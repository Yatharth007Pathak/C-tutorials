#include<stdio.h>

int main() {
    int a = 10;
    int b = 2;
    a += b;  // a = a + b 
    printf("%d \n", a);

    int c = 10;
    int d = 2;
    c -= d;  // c = c - d
    printf("%d \n", c);

    int e = 10;
    int f =2;
    e *= f;  // e = e * f
    printf("%d \n", e);

    int g = 10;
    int h = 2;
    g /= h;  // g = g / h
    printf("%d \n", g);

    int i = 10;
    int j =2;
    i %= j;  // i = i % j
    printf("%d \n", i);
    return 0;
    }

    // assignment operators are      =       +=      -=       *+       /=        %=    