#include<stdio.h>

int main() {
    int i=1;
    while(i<=5) {
        printf("%d \n", i);
        i++;
    }
    
    return 0;
    }

    // while loop

    // While Loop: Repeats a block of code as long as a given condition is true. The condition is checked before each iteration.

    /*
    int i = 1;: Initializes the loop control variable i with the value 1.
    while (i <= 5): A while loop that continues as long as the condition i <= 5 is true.
    i++;: Increments the value of i by 1 in each iteration of the loop.
    */