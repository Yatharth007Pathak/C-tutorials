#include<stdio.h>

int main() {
    for(int i=1; i<=5; i=i+1) {
        printf("Jai Shree Ram \n");
    }
    return 0;
    }

    // for loop

    // For Loop: Executes a block of code a specified number of times, typically controlled by a counter variable.

    /*
    for(int i = 1; i <= 5; i = i + 1) : A for loop that initializes a loop control variable i to 1,
     continues as long as i is less than or equal to 5, and increments i by 1 in each iteration.

     printf("Jai Shree Ram \n");: Inside the loop, this line prints the phrase "Jai Shree Ram" followed by a newline character.
     The loop will run five times, printing "Jai Shree Ram" on each iteration.
    */