#include<stdio.h>

int main() {
     int i=1;
    printf("%d \n", i--);
    printf("%d \n", i);
    printf("%d \n", i);

    printf("hi \n");

int j=1;
    printf("%d \n", --j);
    printf("%d \n", j);
    printf("%d \n", j);  
    return 0;
    }

    // decrement operator
    // i-- use the constant and then decrease it       i++ is post decrement operator
    // --i decrease the constant and then use it       ++i is pre decrement operator